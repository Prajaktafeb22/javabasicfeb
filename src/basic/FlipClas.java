package basic;

public class FlipClas {

	public static void main(String[] args) {
     int firstnum =10 ;int secondnum=20;
     System.out.println("Value of number before Swap");
     System.out.println("Value of First number:"+firstnum);
     System.out.println("Value of Second number:"+secondnum);
     firstnum=firstnum+secondnum;
     secondnum=firstnum-secondnum;
     firstnum=firstnum-secondnum;
     System.out.println("Value of number after Swap");
     System.out.println("Value of First number:"+firstnum);
     System.out.println("Value of Second number:"+secondnum);
     
     
     
     
     
	}

}
